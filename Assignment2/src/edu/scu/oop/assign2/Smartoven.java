private void displaytemperature() {


	String type=(String) (jComboBox2.getSelectedItem());
	double temp= (double) jSpinner1.getValue();
	if(type=="Fahrenheit")
	{

		oven.setTemperature(temp);

	}
	else
	{

		oven.setTemperatureInc(temp);

	}



	if(type=="Fahrenheit")
	{
		tempF.setText((Double.toString(temp)));
		System.out.println("" +oven.getTempinC());
		TempC.setText((Double.toString(this.oven.getTempinC())));

	}

	else    
	{
		TempC.setText(Double.toString(temp));
		tempF.setText(Double.toString(this.oven.getTempinF()));

	}

}                                        


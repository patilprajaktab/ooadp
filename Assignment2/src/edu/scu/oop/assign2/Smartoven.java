private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         

	int cookingtime=(int) jSpinner2.getValue();
	double cookingtemperature=(double)jSpinner1.getValue();
	if(flag==true)
	{
		if(cookingtemperature<=0)
		{

			JOptionPane.showMessageDialog(null, "set the temperture");


		}
		else
		{

			if(cookingtime<=0)


			{

				JOptionPane.showMessageDialog(null, "set the time");
			}


			else
			{


				if(!jCheckBox2.isSelected())
				{

					JOptionPane.showMessageDialog(null, "place the food");

				}


				else
				{

					t = new javax.swing.Timer(1000, new clock());

					t.start();

				}
			}

		}

	}

}
class clock implements ActionListener

{  
	int mealtime =(int) jSpinner2.getValue();
	int count=(mealtime*60);
	@Override

	public void actionPerformed(ActionEvent e) {

		// TODO Auto-generated method stub

		
		int m = count/60;
		int s = count%60;
		count--;
		if(count==-2)

		{

			flag = true;          

			t.stop();

		
		}

		else{

			flag=false;    

			jTextField5.setText("" + m + ":" + s);
		}
	}

}

